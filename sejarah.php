    <p class ="text-light">Bandung Lautan Api merupakan salah satu peristiwa bersejarah di Indonesia. Saat itu, Kota Bandung dikosongkan dan dibumihanguskan. Hal ini dilakukan sebagai upaya agar Bandung tidak dikuasai oleh NICA. Sebab, pada saat itu Indonesia menyadari akan kalah jumlah pasukan dan persenjataan. Advertisement Bagaimana sampai peristiwa ini terjadi? Simak penjelasannya dikutip dari laman Zenius: Bandung Lautan Api merupakan taktik yang digagas oleh Jenderal Nasution dan Sutan Sjahrir untuk menghindari perebutan kekuasaan oleh Belanda atau NICA. Kala itu, warga Bandung beserta anggota TNI rela membakar rumah dan harta benda mereka demi mempertahankan kemerdekaan bangsa Indonesia. Peristiwa ini diawali kedatangan sekutu di bawah pimpinan MacDonald yang ternyata diboncengi oleh Belanda yang masih memiliki keinginan menguasai Indonesia. Bentrokan antara Tentara Keamanan Rakyat (TKR) dan sekutu tidak bisa dihindari, keadaan pun menjadi memanas. Akhirnya, MacDonald menyampaikan ultimatum untuk mengosongkan wilayah Bandung Utara. Sehingga, terbagilah Kota Bandung Utara menjadi wilayah kekuasaan sekutu. Sedangkan, Bandung Selatan dikuasai pemerintah RI. Setelah pembagian kekuasaan tersebut, berbagai pertempuran semakin sering terjadi. Peristiwa Bandung Lautan Api tidak terlepas dari pihak sekutu yang terus mendesak Indonesia menyerahkan seluruh kekuasaan kota Bandung pada pihak sekutu. Diawali pada 12 Oktober 1945, sekutu meminta seluruh senjata yang dimiliki oleh rakyat Indonesia untuk diserahkan pada pihak sekutu. Namun, saat itu rakyat Indonesia tidak mengindahkan ultimatum dari sekutu, sehingga hubungan antara Indonesia dan sekutu menjadi memanas. Pada 24 November 1945, angkatan perang RI menyerang markas sekutu di Bandung bagian utara. Lalu, pada 27 November 1945, sekutu kembali menyampaikan ultimatum supaya rakyat Indonesia dan tentara mengosongkan Bandung Utara, namun lagi-lagi pihak Indonesia tidak menggubris ultimatum tersebut. Pada 23 Maret 1946, sekutu memberi ultimatum kembali kepada Indonesia supaya mengosongkan Bandung Selatan. Melalui musyawarah pada 23 Maret 1946, diputuskanlah untuk membumihanguskan Bandung. Pada saat itu, sebagai komandan Divisi III, Kolonel Abdul Haris Nasution, menyampaikan hasil musyawarah dan memerintahkan agar masyarakat di Kota Bandung segera diungsikan ke tempat aman. Karena merasa senjata dan prajurit Indonesia tidak akan mampu melawan kekuatan dan senjata sekutu, pada 24 Maret 1946 pukul 10.00, Tentara Republik Indonesia (TRI) di bawah pimpinan Kolonel AH Nasution memutuskan membumihanguskan Bandung.</p>
